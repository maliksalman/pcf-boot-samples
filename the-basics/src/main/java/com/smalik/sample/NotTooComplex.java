package com.smalik.sample;

public class NotTooComplex {

	private int val;
	private String name;

	public NotTooComplex(int val, String name) {
		this.val = val;
		this.name = name;
	}

	public int getVal() {
		return val;
	}

	public void setVal(int val) {
		this.val = val;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
